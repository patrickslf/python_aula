#Inicio
#String -> palavras 'utiliza-se aspas simples ou duplas '
computador = 'DELL'
print('A variavel computador é do tipo: ' , type(computador))
#Integer(Iteiro) -> números inteiros exemplo: 1; 2; 3; 4; 5; ...; 1000;
idade = 53
print('A variavel idade é do tipo: ', type(idade))
#Float(Flutuante) -> números fracionados exemplo: 1.3 e 2.5
altura = 1.8
print('A variavel altura é do tipo:' , type(altura))
#Bool(Booleano - > True(Verdadeiro) ou False(Falso)
fumante = True
print('A variavel fumante é do tipo:' , type(fumante))

